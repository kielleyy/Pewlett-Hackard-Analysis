-- Inner join of departments and dept_manager tables
SELECT departments.dept_name,
	dept_manager.emp_no,
	dept_manager.from_date,
	dept_manager.to_date
FROM departments
INNER JOIN dept_manager
ON departments.dept_no = dept_manager.dept_no;

-- with aliases to shorten code -> inner join of departments and dept_manager tables
SELECT d.dept_name,
	de.emp_no,
	de.from_date,
	de.to_date
FROM departments as d
INNER JOIN dept_manager as de
ON d.dept_no = de.dept_no;

-- Joining retirement info and dept_emp in order to get termination dates into retirment info
SELECT retirement_info.emp_no,
	retirement_info.first_name,
	retirement_info.last_name,
	dept_emp.to_date
FROM retirement_info
LEFT JOIN dept_emp
ON retirement_info.emp_no = dept_emp.emp_no;

-- Aliases to shorten code in joining retirement info and dept_emp in order to get termination dates into retirment info
SELECT ri.emp_no,
	ri.first_name,
	ri.last_name,
	de.to_date
INTO current_emp
FROM retirement_info as ri
LEFT JOIN dept_emp as de
ON ri.emp_no = de.emp_no
WHERE de.to_date = ('9999-01-01');

